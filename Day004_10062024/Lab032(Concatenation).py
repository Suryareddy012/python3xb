
#Concatenation
#We cannot concatenate different data types
# we can only concantenate same data types

#eg : STR+STR, INT+INT, Float+Float, Bool+Bool

#String Concatenation
a = "Hello"
b = "World"
print(a+b) #Concatenation
print(a+b) #Sum
print (str(a) + str(b)) #Concatenation

#Integer Concatenation
a = 10
b = 20
print(a+b) #Concatenation
print(a+b) #Sum
print (str(a) + str(b)) #Concatenation
#We can concatenate the int in to String

#Float Concatenation
a = 10.0
b = 20.0
print(a+b) #Concatenation
print(a+b) #Sum
print (str(a) + str(b)) #Concatenation
#We can concatenate the float in to String

#Boolean Concatenation
a = True
b = False
print(a+b) #Concatenation
print(a+b) #Sum
print (str(a) + str(b)) #Concatenation
#We can concatenate the Boolean in to String


